package com.java.BasicsProgram;

public class ToFindOddNoUsingForLoop {

	public static void main(String[] args) {
		
		for (int i = 0; i < 100; i++) {
			
			if (i % 2 != 0) {                                //--- To find Odd no we can use -   (!=) 
		
			System.out.println("Is a Odd no:   " + i);	
			}
			
		}

	}

}
