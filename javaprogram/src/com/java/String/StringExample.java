package com.java.String;

public class StringExample {

	public static void main(String[] args) {
		
		String s1 = "Allahmdllh";  //String Literals
		String s2 = "Subhanallah"; //String Literals
		
		String s3 = new String("Allah Hoakbar"); //creating java string using new keyword
		
		char ch[]={'S','t','r','i','n','g','s'};  //char array
		String s4 = new String(ch); //converting char array to string
		
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
		System.out.println(s4);



	}

}
